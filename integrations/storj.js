let getBlockNumber = () => {

    let client = prepareClient();

    return new Promise(function (resolve, reject) {
        client.eth.getBlockNumber(function (err, result) {

            if (err) {
                console.log("Error : " + err);
                reject(err);
            } else {
                resolve(result);
            }
        });
    })

};

