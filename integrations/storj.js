let transfer = async (toAddress, amount) => {
    const client = prepareClient();
    let contract = prepareContract(client);

    amount = client.utils.toWei(amount, "ether");
    console.log("amount in Wei ", amount);

    client.eth.getTransactionCount(config.FROM_ADDRESS, function (err, nonce) {

        const data = contract.methods.transfer(toAddress, amount).encodeABI();

        console.log("Data : " + data);

        const tx = new Tx({
            nonce: nonce,
            gasPrice: client.utils.toHex(client.utils.toWei('0', 'gwei')),
            gasLimit: 100000,
            to: "0xac8c4a28f79884320009740f4ce0934749d503f5",
            data: data
        });

        tx.sign(new Buffer(config.PRIVATE_KEY, 'hex'));

        const raw = '0x' + tx.serialize().toString('hex');

        console.log(raw);

        client.eth.sendSignedTransaction(raw)
            .on('transactionHash', console.log)
            .on('receipt', console.log)
            .on('error', console.log)
    });
}

